public class Pojo {

	private String prop;

	private String name;

	private int no;

	public Pojo() {
		this.prop="";
		this.name="";
		this.no=0;
	}

	public String getProp() {
		return prop;
	}

	public void setProp(String prop) {
		this.prop = prop;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

}
